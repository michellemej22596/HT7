package model;

public class AssociationEnglishToFrench {

	String englishWord;
	String frenchMeaning;
	/**
	 * @return the englishWord
	 */
	public String getEnglishWord() {
		return englishWord;
	}
	/**
	 * @param englishWord the englishWord to set
	 */
	public void setEnglishWord(String englishWord) {
		this.englishWord = englishWord;
	}
	/**
	 * @return the frenchMeaning
	 */
	public String getSpanishMeaning() {
		return frenchMeaning;
	}
	/**
	 * @param frenchMeaning the frenchMeaning to set
	 */
	public void setSpanishMeaning(String frenchMeaning) {
		this.frenchMeaning = frenchMeaning;
	}
	
}
